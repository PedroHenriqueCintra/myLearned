Imagine que você está criando um sistema para uma escola de magia. Na escola existem casas (como em Hogwarts), e cada casa possui vários 
alunos. 
Além disso, a escola organiza torneios, e cada torneio envolve uma ou mais casas.

Escreva um programa em PHP que permita:
Criar casas e alunos.
Criar torneios e associar casas aos torneios.
Listar todos os torneios, mostrando para cada torneio:
As casas participantes.
Os alunos de cada casa.
Opcional: cada aluno deve poder fornecer seus detalhes (nome e especialidade mágica).